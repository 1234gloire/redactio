// ─── Connexion et insertion ───────────────────────────────────────────────────

async function main() {
  const conn = await mysql.createConnection(DB_URL);

  const buildTerms = (rows, category) =>
    rows.map(([term, synonyms, definition, source, code]) => ({
      term,
      category,
      synonyms: JSON.stringify(synonyms),
      definition,
      source,
      code: code || null,
      active: 1,
      usageCount: 0,
    }));

  const allTerms = [
    ...buildTerms(MEDICAMENTS, "medicament"),
    ...buildTerms(PATHOLOGIES, "pathologie"),
    ...buildTerms(SYMPTOMES, "symptome"),
    ...buildTerms(ANATOMIE, "anatomie"),
    ...buildTerms(BIOLOGIE, "biologie"),
    ...buildTerms(PROCEDURES, "procedure"),
  ];

  console.log(`Insertion de ${allTerms.length} termes...`);

  let inserted = 0;
  let skipped = 0;

  for (const t of allTerms) {
    try {
      await conn.execute(
        `INSERT INTO medical_terms (term, category, synonyms, definition, source, code, active, usageCount)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?)
         ON DUPLICATE KEY UPDATE
           synonyms = VALUES(synonyms),
           definition = VALUES(definition),
           source = VALUES(source),
           code = VALUES(code),
           active = VALUES(active)`,
        [t.term, t.category, t.synonyms, t.definition, t.source, t.code, t.active, t.usageCount]
      );
      inserted++;
    } catch (e) {
      console.warn(`Skipped "${t.term}": ${e.message}`);
      skipped++;
    }
  }

  const [rows] = await conn.execute("SELECT COUNT(*) as total FROM medical_terms");
  console.log(`Terminé. Insérés/mis à jour: ${inserted}, ignorés: ${skipped}`);
  console.log(`Total en base: ${rows[0].total} termes`);

  await conn.end();
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
